#!/usr/bin/env node
/**
 * Generate the Quenderin Facebook content calendar.
 *
 * Assigns the content pool (scripts/social-content.cjs) to Mon/Wed/Fri slots
 * from a start Monday through an end date, rotating the three pillars:
 *   Mon → spotlight   Wed → engineering   Fri → reality
 * Each pool is consumed in order and wraps when exhausted; wrapped entries are
 * flagged `rotated: true` (they land in the Nov–Dec tail — refresh before they
 * publish; the unique launch window is ~14 weeks).
 *
 * Emits docs/social/calendar.json (consumed by post-to-facebook.cjs) and
 * docs/social/calendar.md (human-readable, for review + hand-scheduling).
 *
 * Usage:
 *   node scripts/gen-social-posts.cjs                 # next Monday → 2026-12-31
 *   node scripts/gen-social-posts.cjs --start 2026-07-27 --end 2026-12-31
 */
const fs = require('fs');
const path = require('path');
const pools = require('./social-content.cjs');

const args = process.argv.slice(2);
const argVal = (flag, def) => {
  const i = args.indexOf(flag);
  return i >= 0 && args[i + 1] ? args[i + 1] : def;
};

const iso = (d) => d.toISOString().slice(0, 10);
const DAY = ['Sun', 'Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat'];

function nextMonday(fromISO) {
  const d = new Date(fromISO + 'T00:00:00Z');
  do {
    d.setUTCDate(d.getUTCDate() + 1);
  } while (d.getUTCDay() !== 1);
  return d;
}

const todayISO = new Date().toISOString().slice(0, 10);
const startArg = argVal('--start', null);
const monday = startArg ? new Date(startArg + 'T00:00:00Z') : nextMonday(todayISO);
const end = new Date(argVal('--end', '2026-12-31') + 'T00:00:00Z');

// Mon (offset 0) → spotlight, Wed (2) → engineering, Fri (4) → reality.
const SLOTS = [
  { off: 0, pillar: 'spotlight' },
  { off: 2, pillar: 'engineering' },
  { off: 4, pillar: 'reality' },
];
const idx = { spotlight: 0, engineering: 0, reality: 0 };

const posts = [];
let week = 1;
for (let m = new Date(monday); m <= end; m.setUTCDate(m.getUTCDate() + 7), week++) {
  for (const { off, pillar } of SLOTS) {
    const d = new Date(m);
    d.setUTCDate(d.getUTCDate() + off);
    if (d > end) continue;
    const pool = pools[pillar];
    const i = idx[pillar]++;
    const item = pool[i % pool.length];
    posts.push({
      week,
      date: iso(d),
      day: DAY[d.getUTCDay()],
      pillar,
      id: item.id,
      slug: `${pillar}-${item.id}`,
      image: item.image,
      caption: item.caption,
      link: item.link,
      rotated: i >= pool.length,
    });
  }
}

const outDir = path.join(__dirname, '..', 'docs', 'social');
fs.mkdirSync(outDir, { recursive: true });

// --- calendar.json (machine) ---
fs.writeFileSync(path.join(outDir, 'calendar.json'), JSON.stringify(posts, null, 2) + '\n');

// --- calendar.md (human) ---
const rotatedCount = posts.filter((p) => p.rotated).length;
let md = `# Quenderin — Facebook content calendar\n\n`;
md += `_Generated by \`scripts/gen-social-posts.cjs\`. ${posts.length} posts, `;
md += `${iso(monday)} → ${iso(end)} (${week - 1} weeks), 3×/week Mon·Wed·Fri._\n\n`;
md += `Pillars: **Mon** model/feature spotlight · **Wed** behind the engineering · `;
md += `**Fri** honest reality / privacy / community. Link goes in the FIRST comment `;
md += `(the poster does this automatically).\n\n`;
if (rotatedCount) {
  md += `> ⚠️ ${rotatedCount} posts are marked **↻ rotated** — the pool wrapped for the `;
  md += `Nov–Dec tail. They're honest repeats; refresh their angle before they publish.\n\n`;
}

md += `## Schedule at a glance\n\n`;
md += `| Wk | Date | Day | Pillar | Post | |\n|----|------|-----|--------|------|--|\n`;
for (const p of posts) {
  md += `| ${p.week} | ${p.date} | ${p.day} | ${p.pillar} | ${p.id} | ${p.rotated ? '↻' : ''} |\n`;
}

md += `\n## Full posts\n\n`;
for (const p of posts) {
  md += `### ${p.date} (${p.day}) · ${p.pillar} · ${p.id}${p.rotated ? ' ↻ rotated' : ''}\n\n`;
  md += p.caption
    .split('\n')
    .map((l) => (l ? `> ${l}` : '>'))
    .join('\n');
  md += `\n>\n> 🖼️ ${p.image}\n> 🔗 first comment: ${p.link}\n\n`;
}

fs.writeFileSync(path.join(outDir, 'calendar.md'), md);

console.log(
  `Wrote ${posts.length} posts (${rotatedCount} rotated) → docs/social/calendar.{json,md}\n` +
    `${iso(monday)} → ${iso(end)}, ${week - 1} weeks.`,
);
